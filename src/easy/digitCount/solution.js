function getlength(num) {
    return num.toString().length;
}

console.log("1 character", getlength(1))
console.log("3 characters", getlength(123))
console.log("6 characters", getlength(123456))
console.log("10 characters", getlength(1234567890))
console.log("20 characters", getlength(12345678901234567890))